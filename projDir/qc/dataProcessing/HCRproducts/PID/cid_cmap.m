function x = cid_cmap(n);


if nargin==1 & isempty(n)
  n = size(get(gcf,'Colormap'),1);
end;

cmap = [.9*[.4 0 .8];...
	.9*[.4 0 .8]; ...
	.5*[0 0 1]; ...
	1*[0 0 1]; ...
	.7*[0 0 1]; ...
	1*[.7 .9 1]; ...
	.35*[0 1 0]; ...
	.55*[0 1 0]; ...
	.95*[.72 .90 .3]; ...
	.75*[1 1 1]; ...
	1*[1 .9 .76]; ...
	1*[1 .85 0]; ...
	1*[1 1 .3]; ...
	1*[.83 .64 0]; ...
	.95*[1 .53 .4]; ...
	.95*[1 .67 0];...
	.8*[.77 0 .6]; ...
	.95*[1 .41 0]; ...
	1*[1 0 0]; ...
	1*[1 0 0]];

if nargin < 1
  n = size(cmap,1);
end;

x = interp1(linspace(0,1,size(cmap,1)),cmap(:,1),linspace(0,1,n)','linear');
x(:,2) = interp1(linspace(0,1,size(cmap,1)),cmap(:,2),linspace(0,1,n)','linear');
x(:,3) = interp1(linspace(0,1,size(cmap,1)),cmap(:,3),linspace(0,1,n)','linear');

x = min(x,1);
x = max(x,0);